/* Any Variable */
let favoriteFood:any;
favoriteFood = "Pizza";
console.log(favoriteFood);

favoriteFood = 42;
console.log(favoriteFood);

favoriteFood = true;
console.log(favoriteFood);

favoriteFood = {name: "Andrew"};
console.log(favoriteFood);

favoriteFood = ["Pizza", "Burger"];
console.log(favoriteFood);

let favoriteFruits: any[];
favoriteFruits = ["Apple", "Banana", "Orange"];
console.log(favoriteFruits);

favoriteFruits = [1, 2, 3];
