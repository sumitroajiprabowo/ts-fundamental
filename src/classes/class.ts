/* Classes */
class MyEmployee {
    name: string;
    age: number;

    constructor(name: string, age: number) {
        this.name = name;
        this.age = age;
    }

    greet() {
        console.log(`Hello, my name is ${this.name} and I am ${this.age} years old.`);
    }
}


const employee1 = new MyEmployee("John Doe", 30);
employee1.greet();
